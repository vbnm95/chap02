
public class Character {

	public static void main(String[] args) {
		
		char c1 = 'A';
		char c2 = 66;
		char c3 = 0b01000001;
		char c4 = '가';
		String c5 = "가";
		
		System.out.println("c1=" + c1);
		System.out.println("c2=" + c2);
		System.out.println("c3=" + (int) c3);
		System.out.println("c4=" + (int) c4);
		System.out.println("c5=" + c5);
			
	}

}

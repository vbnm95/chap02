
public class Variable {
	/*
	 * Type 1> Primitive
	 * 			1. 수치
	 * 				- 정수
	 * 					byte
	 * 					short
	 * 					char
	 * 					int
	 * 					long
	 * 				- 실수
	 * 					float
	 * 					double
	 * 			2. 논리
	 * 				- boolean
	 * 
	 * 		2> Reference
	 * 
	 * 
	 */
	public static void main(String[] args) {
		int num = 100;	//변수란 메모리 공간의 이름
		num = num + 100;
		
		System.out.println(num);
		
		//primitive 타입
		//정수
		byte b;			// 1바이트
		short s;		// 2바이트
		char c;			// 2바이트
		int i;			// 4바이트
		long l;			// 8바이트
		//실수
		float f;
		double d;
		//논리
		boolean tf;
		
	}

}